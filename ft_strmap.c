#include "libft.h"

char	*ft_strmap(char const *s, char (*f) (char))
{
	unsigned	i;
	char		*str;

	i = 0;
	if ((!s) || !(str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1))))
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
